package main

import "fmt"

func main() {
	// boolean declaration
	var button bool

	// string declaration
	var greeting string

	// multiple declarations on one line
	var a, b, c int

	// multiple declaration and initialisation
	var d, e, f int = 1, 2, 3

	greeting = "Hello World"

	fmt.Println(button)
	fmt.Println(greeting)
	fmt.Println(a, b, c)
	fmt.Println(d, e, f)
}
